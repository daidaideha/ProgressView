public enum ColorOrientation {
    /***
     * 水平方向 从左到右
     */
    HORIZONTAL,
    /***
     * 垂直方向 从上到下
     */
    VERTICAL,
    /***
     * 从左下角到右上角
     */
    LEFT_TOP_RIGHT,
    /***
     * 从左上角到右下角
     */
    LEFT_BOTTOM_RIGHT
}
